package src.scientificCalculator;

public class Operator {
    int add(int num1, int num2){
        return num1 + num2;
    }

    int subtract(int num1, int num2){
        return num1 - num2;
    }
    int multiply(int num1, int num2){
        return num1 * num2;
    }
    double divide(int dividend, int divisor){
        return dividend / divisor;
    }

}
